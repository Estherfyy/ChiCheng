<pre>
  1、委托方与外商签订外贸合同。
  2、委托方与易恒签订代理合同。
  3、确认HS号、监管条件
  4、易恒收到国外首款（一般在外方汇款3至7天到帐）。
  5、客户将货物明细单(每票都要)与操作移交资料(新客户),交予销售由销售准备与操作的移交事项。
  6、准备出口所需单证，易恒出口制单、出口订舱、出口商检（商检换单，法定商检货物）、出口报关、出口检查（随机检查非法定）。
  7、提单审核，制作、审核议付单据。
  8、委托人审核整套单据，并催款。
  9、易恒收到国外尾款（一般在外方汇款3至7天到帐）。
  10、预录完后，根据操作的开票资料开开增值税发票
  11、委托方向易恒开增值税发票，并附上内贸合同用于退税。(内贸合同时间须比出口时间早一个月,合同编号为该票的代理编号)
  12、易恒审核增值税发票、核销、退税。
