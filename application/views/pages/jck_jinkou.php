      <b>一般的进口代理流程如下</b>
<pre>

  1、委托方与外方核定价格以及贸易条款，生产周期，运输方式等信息。
  2、委托方与易恒签订进口代理合同。
  3、确认HS号、监管条件（商检，机电证，重工证，3c等进口必要证书）
  4、易恒收到委托方货款（根据订单支付条款）。
  5、客户将货物明细单，发票(每票都要)交予销售，由销售准备与操作的移交事项。新客户需交予操作联系方式，联系人以及代理费率等收费价格。
  6、操作根据订单信息准备进口付汇资料，进口如需出具机电证等需提前办理的资料。
  7、外方备好货物，根据实际情况提供客户合适运输方式建议，安排货代运输。（木质包装注意让老外提供熏蒸证明，木箱上印上清楚的IPPC标示）
  8、操作收到外方提供进口单据安排进口报关事宜（货物到港后）一般清关完成为3-4个工作日，若查验则需根据实际情况顺延
  9．放行 送货 一般为2个工作日


  进口通关速度（公司自有报关员）：
  空运：二个工作日
  海运： 海运公司允许换单后3到7个工作日
</pre>

