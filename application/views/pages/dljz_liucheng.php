      <b>企业代理记帐的流程</b>
<pre>
一、代理记帐操作流程

	1、 每月下旬，委托方提供原始票据，以快递方式或者我司人员上门收取。
	2 、每月月底，我司财务人员用财务软件进行 制单，会计人员审核、结转、记账，编制财务报表、纳税申报表。
	3 、每月月初，我司代为进行纳税申报，提交委托方财务报表，并提供合理的财税建议。
	4 、进出口退税与外汇核销：每月一次，派专业财务人员前往办理及自行办理。

二、委托方需提供的资料主要

	1、 现金单据（差旅费、业务招待费、员工工资、社保费、通讯费、运输费、邮递费等）
	2、 银行单据（提现、转帐、贷记凭证、电汇、进帐单、借款单等）
	3、 发票（本月 1 日 ---31 日开具的所有发票）
	4、 增值税清单（销项汇总清单、销项明细清单）
	5、 报表上所需盖的公章
	6 、其它所需资料
