			<b>进出口代理</b>
      <p>进出口代理是指对于有货物进出口需求的客户，
        由于对进出口业务的不熟悉或者是没有进出口权，
        通过进出口代理公司代办进口出口的贸易服务型业务。
        进出口代理一般由代理商操作，在进出口的过程中，
        进出口代理商作为发货人和收货人之外的中间人，在操作过程中收取佣金，
        即服务费，但一般不承担信用、汇兑和市场风险，不拥有进口商品的所有权，
        现代物流企业有实力的，也同时提供比如备货融资，进口资金代垫等服务，
        一般资金需求大的时侯方需要这种服务，是供应链管理的一种模式。进出口代理细分为进口代理和出口代理。
      </p>

